#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	z;

	z = 0;
	while (s[z] != '\0')
		z = z + 1;
	while (z >= 0)
	{
		if (s[z] == (char)c)
			return ((char *)s + z);
		z--;
	}
	return (0);
}
