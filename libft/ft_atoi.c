#include "libft.h"

int	ft_atoi(char *str)
{
	int				i;
	int				b;
	int				k;

	i = 0;
	b = 0;
	k = 0;
	while ((str[i] > 8 && str[i] < 14) || str[i] == 32)
		i++;
	if (str[i] == 45 || str[i] == 43)
	{
		if (str[i] == 45)
			k = k + 1;
		i++;
	}
	while (ft_isdigit(str[i]) == 1)
	{
		b = b * 10 + (str[i] - '0');
		i++;
	}
	if (k == 1)
		return (-b);
	else
		return (b);
}
